package TYPE_CASTING;

public class EXPLICIT_TYPE_casting {
	
	public void explicit()
	{
		
		// Explicit type casting is conversion of bigger data type into smaller 
		
		System.out.println(" EXPLICIT type casting ");
		
		double a=100;
		
		float b=(float) a; // here the explicit type casting is done 
		
		long c=200;
		
		int d= (int) c;   
		
		System.out.println(" VALUE OF double   a: "+a);
		System.out.println(" VALUE OF float    b: "+b);
		System.out.println(" VALUE OF long     c: "+c);
		System.out.println(" VALUE OF int      d: "+d);
		
	}
	
	

}
